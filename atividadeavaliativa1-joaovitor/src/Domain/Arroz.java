package Domain;

public class Arroz {
	
	
	private double sacas;	
	private double valorcota;
	private double numeromesescota;
	private double valormeses;	

	public double getSacas() {
		return sacas;
	}

	public void setSacas(double sacas) {
		this.sacas = sacas;
	}

	
	public double getValorcota() {
		return valorcota;
	}

	public void setValorcota(double valorcota) {
		this.valorcota = valorcota;
	}

	public double getNumeromesescota() {
		return numeromesescota;
	}

	public void setNumeromesescota(double numeromesescota) {
		this.numeromesescota = numeromesescota;
	}

	public double getValormeses() {
		return valormeses;
	}

	public void setValormeses(double valormeses) {
		this.valormeses = valormeses;
	}
	
	
	

}
