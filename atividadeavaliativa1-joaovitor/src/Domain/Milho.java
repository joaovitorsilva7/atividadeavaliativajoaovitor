package Domain;

public class Milho {
	
	
	
	public String mesesdecota;
	public String safra;
	private double valorsacas;
	private double anualvalor;
	private double valormeses;
				
	
	public String getSafra() {
		return safra;
	}

	public void setSafra(String safra) {
		this.safra = safra;
	}

	public double getValorsacas() {
		return valorsacas;
	}

	public void setValorsacas(double valorsacas) {
		this.valorsacas = valorsacas;
	}

	public String getMesesdecota() {
		return mesesdecota;
	}

	public void setMesesdecota(String mesesdecota) {
		this.mesesdecota = mesesdecota;
	}

	public double getAnualvalor() {
		return anualvalor;
	}

	public void setAnualvalor(double anualvalor) {
		this.anualvalor = anualvalor;
	}

	public double getValormeses() {
		return valormeses;
	}

	public void setValormeses(double valormeses) {
		this.valormeses = valormeses;
	}
	
	

}
